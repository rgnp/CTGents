"""自进化编排器 — 研究→综合→生成→验证→合入/回滚 的完整闭环。

由 /evolve 命令触发，通过注入 system prompt 驱动 LLM 自主完成整个流程。

流程：
  1. 研究阶段：多源搜索 + 交叉验证 + 提取模式
  2. 综合阶段：组合模式生成 2-3 个候选方案
  3. 生成阶段：LLM 落地代码修改
  4. 验证阶段：运行 validate.py 流水线
  5. 决策阶段：合入或回滚，记录进化档案
"""

from dataclasses import dataclass, field

# ═══════════════════════════════════════════════════════════════
# 进化 prompt 模板
# ═══════════════════════════════════════════════════════════════

RESEARCH_PROMPT = """## 自进化研究阶段

目标：{goal}

请按以下步骤进行深入研究（不要修改代码）：

### 第1步：检查历史
1. 调用 `evolve_query` 搜索与此目标相关的历史进化记录
2. 注意过去的失败教训（尤其是 outcome=reverted 的记录）
3. 如果有类似目标的成功经验，记下他们是怎么做的

### 第2步：多源搜索
1. 使用 `search_web` 从至少 3 个不同来源搜索相关最佳实践
2. 对于有代码示例的搜索结果，使用 `read_page` 深入阅读
3. 对每个来源评估可信度：
   - 官方文档 > 知名技术博客 > 论坛帖子
   - 有完整代码示例的 > 只有文字描述的
   - 最近更新的 > 过时的

### 第3步：模式提取
从搜索到的内容中提取：
- 核心设计模式或算法
- API 签名和用法
- 常见的陷阱和注意事项
- 性能对比数据（如果有）

### 第4步：输出研究报告
用结构化格式输出你的发现：
1. 历史教训（如果有）
2. 来源汇总（每个来源的URL、可信度、关键发现）
3. 提取的模式列表
4. 推荐 2-3 种可能的实现方案（每种说明优缺点）

完成后说"研究阶段完成"，等待下一阶段指令。"""


SYNTHESIS_PROMPT = """## 自进化综合阶段

基于你的研究报告，生成 2-3 个候选实现方案。

对每个方案说明：
- **方案ID**: A / B / C
- **描述**: 简要说明实现思路
- **优点**: 列出 3+ 个优点
- **缺点**: 列出 2+ 个缺点或风险
- **复杂度**: low / medium / high
- **风险等级**: low / medium / high
- **需要修改的文件**: 列出具体文件路径
- **需要创建的文件**: 如果有
- **支持的来源**: 哪些研究来源支持此方案

然后对所有方案进行排序，推荐最佳方案并说明理由。

完成后说"综合阶段完成"。"""


GENERATION_PROMPT = """## 自进化生成阶段

基于你选择的最佳方案，现在开始实现代码修改。

### 修改前检查
1. 调用 `evolve_check_access` 检查每个目标文件的修改权限
2. 如果有文件被锁定，调用 `evolve_suggest_tests` 了解如何解锁
3. 如果需要提升覆盖率，可以先添加测试

### 安全快照
1. 所有修改前，调用 `git_commit` 保存当前状态

### 实现代码
1. 使用 `write_file` 或 `edit_file_lines` 逐个文件修改
2. 每个文件改完后确保语法正确
3. 如果需要新依赖，记录清楚

### 验证
1. 改完所有文件后，调用 `evolve_validate` 运行完整验证流水线
2. 如果验证失败，分析原因并修复（最多尝试 2 次修复）
3. 如果 2 次修复后仍失败，使用 `undo_edit` 回滚，然后尝试备选方案

### 完成
验证通过后，调用 `evolve_query` 确认没有引入已知的不良模式。
最后说"自进化完成"，并总结做了什么。"""


# ═══════════════════════════════════════════════════════════════
# 系统 prompt 构建
# ═══════════════════════════════════════════════════════════════

def build_evolution_system_prompt(goal: str) -> str:
    """构建完整的自进化系统 prompt，注入到 ctx.log。

    这个 prompt 会被 LLM 看到，驱动它完成研究→综合→生成→验证的全流程。
    """
    parts = [
        "## ⚙️ 自进化模式已激活",
        "",
        f"**目标**: {goal}",
        "",
        "你将按照以下流程自主完成代码进化：",
        "",
        "```",
        "研究 ──→ 综合 ──→ 生成 ──→ 验证 ──→ 合入/回滚",
        "  │         │         │         │           │",
        "  │ 多源搜索 │ 组合模式 │ 写代码   │ 跑测试    │ 成功→commit",
        "  │ 交叉验证 │ 排序候选 │ 改文件   │ 查覆盖率  │ 失败→rollback",
        "  │ 提取模式 │ 选最优   │ git快照  │ 无新lint  │ 记录档案",
        "  └─────────┴─────────┴─────────┴───────────┘",
        "```",
        "",
        "### 关键工具",
        "- `search_web` / `read_page` — 多源搜索和学习",
        "- `evolve_query` — 查询历史进化记录，避免重复踩坑",
        "- `evolve_check_access` — 检查文件修改权限",
        "- `evolve_coverage` — 查看覆盖率门禁状态",
        "- `evolve_suggest_tests` — 获取解锁修改权限的测试建议",
        "- `evolve_validate` — 运行完整验证流水线",
        "- `evolve_status` — 进化系统状态总览",
        "",
        "### 安全规则",
        "1. 修改代码前必须 `git_commit` 做快照",
        "2. 改完后必须 `evolve_validate` 验证",
        "3. 验证失败必须回滚或修复",
        "4. 同样的错误不犯两次（先查 `evolve_query`）",
        "5. 修改 `src/guard.py` 或 `src/watchdog.py` 需要最高覆盖率（查 `evolve_coverage`）",
        "",
        "### 输出规范",
        "每个阶段完成后给出明确的「阶段完成」标记，便于追踪进度。",
        "最终的修改总结要包含：改了什么文件、为什么这样改、学到了什么。",
        "",
        "现在开始。首先调用 `evolve_status` 了解当前状态。",
    ]
    return "\n".join(parts)


def build_research_prompt(goal: str) -> str:
    """构建纯研究模式 prompt（/research 命令使用）。"""
    return RESEARCH_PROMPT.format(goal=goal)
